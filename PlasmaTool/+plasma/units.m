function u = units()
%PLASMA.UNITS Simple unit helpers and species defaults.

u.cm3_to_m3 = 1e6;     % number density: [cm^-3] -> [m^-3]
u.keV_to_eV = 1e3;

% Common species molecular weights [amu]
u.species = struct(...
    'H', 1.00784, ...
    'D', 2.01410, ...
    'T', 3.01605, ...
    'He', 4.002602, ...
    'Ar', 39.948);
end
